    private void btnEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEquipoActionPerformed
        // TODO add your handling code here:
        if (!txtEquipo.getText().isEmpty()) {
            //Crear un nuevo equipo
            Equipos obj = new Equipos();
            obj.setNombre(txtEquipo.getText());
            //Se agrega a la lista
            this.lstEquipos.add(obj);
            //Agregarlo a todos los combobox de equipos
            this.cmbEquipos.addItem(txtEquipo.getText());
            this.cmbEq1.addItem(txtEquipo.getText());
            this.cmbEq2.addItem(txtEquipo.getText());
            txtEquipo.setText("");
            JOptionPane.showMessageDialog(this, "Equipo agregado correctamente!");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Por favor ingrese un nombre para el equipo");
        }
    }//GEN-LAST:event_btnEquipoActionPerformed

    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        // TODO add your handling code here:
        //Verificar que se seleccione un equipo y un jugador
        if (cmbEquipos.getSelectedIndex() > 0 && cmbJugadores.getSelectedIndex() > 0) {
            //Se agrega el jugador a la lista de jugadores del equipo asignado
            Jugadores jugador = this.lstJugadores.get(cmbJugadores.getSelectedIndex()-1);
            this.lstEquipos.get(cmbEquipos.getSelectedIndex()-1).setLstJuga(jugador);
            JOptionPane.showMessageDialog(this, "Jugador agregado correctamente!");
            cmbEquipos.setSelectedIndex(0);
            cmbJugadores.setSelectedIndex(0);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Seleccione un jugador y un equipo");
        }
    }//GEN-LAST:event_btnAsignarActionPerformed

    //Metodo para llenar la tabla segun la lista
    private void ActulizarTabla(JTable tabla, List<Jugadores> lista) {
        tabla.setSelectionBackground(new Color(255, 255, 204));
        tabla.setSelectionForeground(Color.BLACK);
        DefaultTableModel model = (DefaultTableModel)tabla.getModel();
        while(model.getRowCount()>0){model.removeRow(0);} //Limpiar modelo
        //Se agregan todos los que esten en la lista
        for (Jugadores temp : lista)
        {
            model.addRow(new Object[]{temp.getNombre(), temp.getEdad(), temp.getAltura(), temp.getPeso()});
        }

    }
    
    private void btnjugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnjugarActionPerformed
        // TODO add your handling code here:
        if (cmbEq1.getSelectedIndex() > 0 && cmbEq2.getSelectedIndex() > 0) {
            //Se utiliza el metodo de ActulizarTabla para llenarlas segun el equipo seleccionado
            ActulizarTabla(tblEquipo1, this.lstEquipos.get(cmbEq1.getSelectedIndex()-1).getLstJuga());
            ActulizarTabla(tblEquipo2, this.lstEquipos.get(cmbEq2.getSelectedIndex()-1).getLstJuga());
        }
    }//GEN-LAST:event_btnjugarActionPerformed

