    //Metodo para calcular el mas alto
    private void MasAlto(){
        //Se crea una clase de un jugador;
        Jugadores jugador;
        try {
            //Se le asiga el primer valor de la lista para compararlo, por ahora es el mas alto;
            jugador = lstJugadores.get(0);
            //Recorre toda la lista
            for (Jugadores temp : lstJugadores){
                //Si el jugador temp es mas alto al actual jugador mas alto, entonces el jugar mas alto se vuelve temp;
                if (temp.getAltura() > jugador.getAltura())
                {
                    jugador = temp;
                }
            }
            JOptionPane.showMessageDialog(this, "El jugador más alto es: " + jugador.getNombre());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No hay jugadores");
        }
    }
    
    //Metodo para calcular el menor de edad
    private void MenorEdad(){
        Jugadores jugador;
        try {
            //Por ahora el primer jugador es el de menor edad, luego se compara con el resto
            jugador = lstJugadores.get(0);
            //Se recorre la lista
            for (Jugadores temp : lstJugadores){
                //Si el jugador temp es de menor edad que el jugador atual, el nuevo jugador de menor edad sera temp
                if (temp.getEdad()< jugador.getEdad())
                {
                    jugador = temp;
                }
            }
            JOptionPane.showMessageDialog(this, "El jugador de menor edad es: " + jugador.getNombre());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No hay jugadores");
        }
    }
    
    //Metodo para descubrir el jugador de mayor peso
    private void MayorPeso(){
        Jugadores jugador;
        try {
            //Actualmente el de mayor peso es el primero
            jugador = lstJugadores.get(0);
            //Se recorre la lista
            for (Jugadores temp : lstJugadores){
                //Si el jugador temp es de mayor peso que el jugador actual, el nuevo jugador de mayor peso sera temp
                if (temp.getPeso() > jugador.getPeso())
                {
                    jugador = temp;
                }
            }
            JOptionPane.showMessageDialog(this, "El jugador de mayor peso es: " + jugador.getNombre());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No hay jugadores");
        }
    }
    
    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        if (!(txtNombre.getText().isEmpty() && txtEdad.getText().isEmpty() && txtAltura.getText().isEmpty() && txtPeso.getText().isEmpty())){
            try {
                //Se registra un nuevo jugador
                String nombre = txtNombre.getText().trim();
                int edad = Integer.parseInt(txtEdad.getText());
                double altura = Double.parseDouble(txtAltura.getText());
                double peso = Double.parseDouble(txtPeso.getText());
                Jugadores obj = new Jugadores(nombre, edad, altura, peso);
                //Se agrega a la lista de jugadores
                this.lstJugadores.add(obj);
                //Se agrega a los jugadores del combobox
                this.cmbJugadores.addItem(nombre);
                limpiar(); //Metodo limpiar
                JOptionPane.showMessageDialog(this, "Datos ingresados correctamente");
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Ingrese solo datos validos.");
            }
            
           
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

